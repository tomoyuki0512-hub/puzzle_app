// ============================================================
//  なんいど（ピースすう）の設定 — ここが唯一の定義元
// ------------------------------------------------------------
//  ピース数を変えたい／段階を増やしたいときは、この配列を編集
//  するだけで、選択画面もパズル盤も自動で追従します。
//
//  cols x rows = ピース数。
//  ここが1つだけのときは「なんいど選び」画面を自動でスキップし、
//  カードを押すとそのままパズルが始まります（views/home.js で判定）。
//  段階を増やしたいときは要素を足すだけ（例: 4/9/16 の3段階）。
//     { id: 'd4',  label: '4ピース',  short: '4',  cols: 2, rows: 2 },
//     { id: 'd9',  label: '9ピース',  short: '9',  cols: 3, rows: 3 },
// ============================================================

export const difficulties = [
  { id: 'd16', label: '16ピース', short: '16', cols: 4, rows: 4 }, // 16 (4x4)
];

export function difficultyById(id) {
  return difficulties.find((d) => d.id === id) || difficulties[0];
}

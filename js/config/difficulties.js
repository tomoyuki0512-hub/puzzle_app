// ============================================================
//  なんいど（ピースすう）の設定 — ここが唯一の定義元
// ------------------------------------------------------------
//  ピース数を変えたい／段階を増やしたいときは、この配列を編集
//  するだけで、選択画面もパズル盤も自動で追従します。
//
//  cols x rows = ピース数。写真が横長(16:9)なので cols を多めに。
//  例) 4/9/16 など小さい子向けに下げたい場合はここを書き換える。
// ============================================================

export const difficulties = [
  { id: 'd40', label: '40ピース', short: '40', cols: 8,  rows: 5 }, // 40
  { id: 'd50', label: '50ピース', short: '50', cols: 10, rows: 5 }, // 50
  { id: 'd60', label: '60ピース', short: '60', cols: 10, rows: 6 }, // 60
  { id: 'd80', label: '80ピース', short: '80', cols: 10, rows: 8 }, // 80
];

export function difficultyById(id) {
  return difficulties.find((d) => d.id === id) || difficulties[0];
}
